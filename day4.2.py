# Integers
x = 5  # Creating an integer variable
y = 10

# Basic arithmetic operations
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
floor_division = x // y
exponentiation = x ** y
modulo = x % y

# Floats
a = 3.14  # Creating a float variable
b = 2.5

# Basic arithmetic operations
addition_float = a + b
subtraction_float = a - b
multiplication_float = a * b
division_float = a / b
exponentiation_float = a ** b

# Type conversion
int_to_float = float(x)
float_to_int = int(a)

# Built-in functions
abs_value = abs(-5)
max_value = max(x, y)
min_value = min(a, b)
round_value = round(a, 1)

# Printing the results
print("Integer operations:")
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Floor Division:", floor_division)
print("Exponentiation:", exponentiation)
print("Modulo:", modulo)

print("\nFloat operations:")
print("Addition:", addition_float)
print("Subtraction:", subtraction_float)
print("Multiplication:", multiplication_float)
print("Division:", division_float)
print("Exponentiation:", exponentiation_float)

print("\nType conversion:")
print("Integer to Float:", int_to_float)
print("Float to Integer:", float_to_int)

print("\nBuilt-in functions:")
print("Absolute value:", abs_value)
print("Maximum value:", max_value)
print("Minimum value:", min_value)
print("Rounded value:", round_value)
